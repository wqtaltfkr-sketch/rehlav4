-- إضافة عمود transcript إلى content_media
-- الهدف: تخزين النص المفرّغ (Transcript) لأي فيديو/مصدر وسائط،
-- بحيث يستطيع المساعد الذكي (ask-lesson-ai) الإجابة عن أسئلة
-- تخص محتوى الفيديو الفعلي، وليس فقط عنوانه ورابطه.
--
-- بدون NOT NULL عمداً: كثير من عناصر content_media الحالية
-- والمستقبلية لن يكون لها نص مفرّغ (مثلاً روابط PDF أو روابط عامة)،
-- ويجب أن تستمر بالعمل تماماً كما كانت دون أي تغيير.
-- ============================================================
ALTER TABLE content_media
    ADD COLUMN IF NOT EXISTS transcript TEXT;

COMMENT ON COLUMN content_media.transcript IS
    'النص المفرّغ (Transcript) لمحتوى الفيديو/المصدر — اختياري. '
    'يُستخدم كسياق إضافي يُرسل للمساعد الذكي (ask-lesson-ai) '
    'ليتمكن من الإجابة عن أسئلة الطالب المتعلقة بمحتوى الفيديو الفعلي، '
    'لا فقط عنوانه أو رابطه. القيمة الفارغة (NULL) تعني عدم وجود نص مفرّغ '
    'لهذا العنصر، ولا تؤثر على عمل التطبيق.';
